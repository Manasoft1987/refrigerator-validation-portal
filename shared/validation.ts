/**
 * Расчёт минимального количества регистраторов по ЕАЭК (Рек. №8, п. 16д).
 * Возвращает количество точек по длине / ширине / вертикали и итог.
 */
export function computeWarehouseSensorCount(opts: {
  lengthM?: number | null;
  widthM?: number | null;
  heightM?: number | null;
  externalEnv?: boolean;
}): {
  nL: number;
  nW: number;
  nV: number;
  base: number;
  external: number;
  total: number;
  rationale: string;
} {
  const horiz = (m: number | null | undefined): number => {
    if (!m || m <= 0) return 0;
    if (m <= 10) return 2;
    if (m <= 40) return 3;
    if (m <= 60) return 4;
    return 5;
  };
  const vert = (m: number | null | undefined): number => {
    if (!m || m <= 0) return 0;
    if (m <= 1.5) return 1;
    if (m < 5) return 2;
    return 3;
  };
  const nL = horiz(opts.lengthM ?? null);
  const nW = horiz(opts.widthM ?? null);
  const nV = vert(opts.heightM ?? null);
  const base = nL && nW && nV ? nL * nW * nV : 0;
  const external = opts.externalEnv ? 1 : 0;
  const total = base + external;
  const rationale =
    `Длина ${opts.lengthM ?? "—"} м → ${nL} точек; ширина ${opts.widthM ?? "—"} м → ${nW} точек; ` +
    `высота ${opts.heightM ?? "—"} м → ${nV} ярус(а). База: ${nL}×${nW}×${nV} = ${base}.` +
    (external ? " + 1 внешний регистратор (контакт с внешней средой)." : "");
  return { nL, nW, nV, base, external, total, rationale };
}

