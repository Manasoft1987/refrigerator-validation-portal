export const EQUIPMENT_KIND_LABELS: Record<WarehouseEquipmentKind, string> = {
  conditioner: "Кондиционер",
  ventilation: "Приточно-вытяжная вентиляция",
  heat_curtain: "Тепловая завеса",
  chiller: "Чиллер",
  fan_coil: "Фанкойл",
  other: "Другое оборудование",
};

type EquipmentRow = { kind?: string | null; name?: string | null };

export function buildWarehouseQuestions(
  equipment: EquipmentRow[],
  stage: "iq" | "oq",
): string[] {
  const common = stage === "iq" ? COMMON_IQ : COMMON_OQ;
  const kindMap = stage === "iq" ? KIND_IQ : KIND_OQ;

  const seenKindsSet: WarehouseEquipmentKind[] = [];
  const seen = new Set<string>();
  for (const eq of equipment) {
    const k = (eq.kind ?? "other") as WarehouseEquipmentKind;
    if (!seen.has(k)) {
      seen.add(k);
      seenKindsSet.push(k);
    }
  }

  const result: string[] = [...common];

  for (const kind of seenKindsSet) {
    const label = (EQUIPMENT_KIND_LABELS as Record<string, string>)[kind] ?? kind;
    const kindQuestions = (kindMap as Record<string, string[]>)[kind] ?? kindMap.other;
    for (const question of kindQuestions) {
      result.push(`[${label}] ${question}`);
    }
  }

  return result;
}
